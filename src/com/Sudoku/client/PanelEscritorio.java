package com.Sudoku.client;

import com.google.gwt.user.client.ui.AbsolutePanel;

public class PanelEscritorio {

	private AbsolutePanel panelEscritorio;
	private ClientController controller;

	public PanelEscritorio(ClientController controller) {
		super();
		this.controller = controller;
		panelEscritorio = new AbsolutePanel();
		panelEscritorio.setSize("1024px", "768px");
	}

	/**
	 * 
	 * @return el AbsolutePanel sobre el que se pueden insertar Widgets
	 */
	AbsolutePanel getPanelEscritorio() {
		return panelEscritorio;
	}
}
